package livres;

public class Pays {
    public String nom;
    public int code;



    public Pays(String nom) {
        this.nom = nom;
        this.code = code;
    }

    public String getNom() {
        return nom;
    }

    public int getCode() {
        return code;
    }


    public void setCode(int code) {
        this.code = code;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }
}
